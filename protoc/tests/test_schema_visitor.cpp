TEST(SchemaVisitor, CppAndIpcByteIdentical) {
    Fixtures fx;

    for (const auto& c : Cases()) {
        SCOPED_TRACE(c.message);
        const Descriptor* msg = fx.Get(c.message);
        ASSERT_NE(msg, nullptr) << "fixture message not found: " << c.message;

        // (1) Anti-drift: the IR-driven nanoarrow schema is byte-identical to the
        // committed golden captured from today's emitter.
        const std::vector<uint8_t> golden = ReadFileBytes(GoldenDir() / c.golden);
        ASSERT_FALSE(golden.empty())
            << "missing golden " << c.golden
            << " (regenerate with FLETCHER_CAPTURE_GOLDENS=1)";
        const std::vector<uint8_t> actual = IrSchemaIpc(msg);
        EXPECT_EQ(actual, golden) << "IR schema drifted from golden for " << c.message;

        // (2) One visitor, both sinks agree: the C++ source sink and the nanoarrow
        // sink receive an identical ordered stream of operations.
        EXPECT_EQ(TraceCpp(msg), TraceNano(msg))
            << "C++ source and in-process schema paths diverged for " << c.message;
    }
}

// Golden regeneration helper. Inert unless FLETCHER_CAPTURE_GOLDENS is set; when
// set it writes each golden from the LEGACY in-process BuildMessageSchema (the
// pre-migration ground truth) into SCHEMA_GOLDEN_DIR and then skips.
TEST(SchemaVisitor, CaptureGoldens) {
    const char* capture = std::getenv("FLETCHER_CAPTURE_GOLDENS");
    if (!capture || std::string(capture).empty()) {
        GTEST_SKIP() << "set FLETCHER_CAPTURE_GOLDENS=1 to (re)capture goldens";
    }

    Fixtures fx;
    std::filesystem::create_directories(GoldenDir());
    for (const auto& c : Cases()) {
        const Descriptor* msg = fx.Get(c.message);
        ASSERT_NE(msg, nullptr) << c.message;
        nanoarrow::UniqueSchema schema = fletcher::BuildMessageSchema(msg);
        const std::vector<uint8_t> bytes = fletcher::SerializeSchemaIpc(schema.get());
        const std::filesystem::path path = GoldenDir() / c.golden;
        std::ofstream out(path, std::ios::binary);
        ASSERT_TRUE(out) << "cannot write " << path;
        out.write(reinterpret_cast<const char*>(bytes.data()),
                  static_cast<std::streamsize>(bytes.size()));
    }
    SUCCEED() << "captured " << Cases().size() << " goldens into " << GoldenDir().string();
}
