// GIR-5 forcing test: the unified schema visitor drives BOTH schema paths — the
// generated C++ <Class>Schema() source (CppSchemaSink) and the in-process / IPC
// ArrowSchema (NanoarrowSchemaSink) — and they agree.
//
// Two guarantees are checked per node-kind fixture:
//
//   1. .ipc byte identity: the schema built through the IR-driven nanoarrow sink
//      (BuildMessageSchemaIntoFromIr), serialized to Arrow IPC, is byte-identical
//      to a committed golden captured from today's pre-migration emitter. This is
//      the anti-drift guard; it is the red-first cutover signal (a stubbed visitor
//      mismatches the golden) and stays green once the visitor emits correctly.
//
//   2. one visitor, both sinks agree: running the SAME SchemaVisitor with the C++
//      source sink and with the nanoarrow sink issues an identical ordered stream
//      of sink operations (types, names, nullability, metadata, deep-copies). A
//      recording decorator wraps each real sink and the two op traces must match,
//      proving the source-render and direct-exec paths cannot diverge.
//
// The goldens are regenerated by running with FLETCHER_CAPTURE_GOLDENS=1 (which
// writes them from the legacy in-process BuildMessageSchema into SCHEMA_GOLDEN_DIR
// and then skips); normal runs read the committed files.

